# ── Interactive prompt ────────────────────────────────────────────────────────

def prompt(question, default=None, choices=None, free_text=False):
    """Prompt with optional default and choice validation."""
    hint = ""
    if choices and not free_text:
        hint = f"  [{' / '.join(choices)}]"
    if default is not None:
        hint += f"  (default: {default})"
    while True:
        raw = input(f"  {question}{hint}: ").strip()
        if not raw and default is not None:
            return str(default)
        if raw and free_text:
            return raw
        if raw and choices:
            matches = [c for c in choices if c.lower().startswith(raw.lower())]
            if len(matches) == 1:
                return matches[0]
            elif len(matches) > 1:
                print(_yellow(f"    Ambiguous — did you mean: {', '.join(matches)}?"))
                continue
            else:
                # Allow exact match regardless of case
                exact = [c for c in choices if c.lower() == raw.lower()]
                if exact:
                    return exact[0]
                print(_yellow(f"    Invalid. Choose from: {', '.join(choices)}"))
                continue
        if raw:
            return raw
        print(_yellow("    Please enter a value."))

# ── Step 1: OneMap address lookup ─────────────────────────────────────────────

def onemap_search(query: str) -> list:
    import requests
    url = ONEMAP_SEARCH_URL.format(query=query.replace(" ", "+"))
    try:
        resp = requests.get(url, timeout=10)
        resp.raise_for_status()
        return resp.json().get("results", [])
    except Exception as e:
        print(_yellow(f"    [Warning] OneMap API error: {e}"))
        return []

def resolve_address(address_query: str) -> dict:
    """
    Query OneMap. Returns dict with POSTAL, BUILDING, ADDRESS, LATITUDE, LONGITUDE.
    Returns None if nothing found.
    """
    results = onemap_search(address_query)
    if not results:
        return None

    if len(results) == 1:
        chosen = results[0]
    else:
        print(f"\n  Found {len(results)} matching addresses:")
        for i, r in enumerate(results[:10], 1):
            print(f"    {i}. {r.get('ADDRESS', 'N/A')}  [Postal: {r.get('POSTAL', '?')}]")
        while True:
            sel = input(f"\n  Select (1-{min(len(results), 10)}, or 0 to enter postal manually): ").strip()
            if sel == "0":
                return None
            try:
                idx = int(sel) - 1
                if 0 <= idx < min(len(results), 10):
                    chosen = results[idx]
                    break
            except ValueError:
                pass
            print(_yellow("    Invalid selection."))

    postal = chosen.get("POSTAL", "").strip()
    if not postal or postal == "NIL":
        postal = None

    return {
        "POSTAL"   : postal,
        "BUILDING" : chosen.get("BUILDING", chosen.get("SEARCHVAL", "Unknown")),
        "ADDRESS"  : chosen.get("ADDRESS", ""),
        "LATITUDE" : float(chosen.get("LATITUDE", 0) or 0),
        "LONGITUDE": float(chosen.get("LONGITUDE", 0) or 0),
    }

# ── Step 2: Postal lookup ─────────────────────────────────────────────────────

def load_postal_lookup() -> dict:
    import pandas as pd
    if not POSTAL_LOOKUP_CSV.exists():
        raise FileNotFoundError(
            f"postal_lookup.csv not found at: {POSTAL_LOOKUP_CSV}\n"
            "Download it from Perplexity and place it in the same folder as this script."
        )
    ldf = pd.read_csv(POSTAL_LOOKUP_CSV, dtype={"Postal Code": str})
    ldf["Postal Code"] = ldf["Postal Code"].str.zfill(6)
    return ldf.set_index("Postal Code").to_dict("index")

# ── Step 3: Floor level from unit number ──────────────────────────────────────

def extract_floor(unit_number: str) -> int:
    """#12-34 → 12, #B1-05 → 0, 25-10 → 25"""
    s = unit_number.strip().lstrip("#").upper()
    if s.startswith("B"):
        return 0
    m = re.match(r"^(\d+)", s)
    return int(m.group(1)) if m else 0

